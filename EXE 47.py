# Write a Python program to convert a given string into a list of words. Go to the editor
# Sample Output:
# ['The', 'quick', 'brown', 'fox', 'jumps', 'over', 'the', 'lazy', 'dog.']
# ['The', 'quick', 'brown', 'fox', 'jumps', 'over', 'the', 'lazy', 'dog.']

# ANSWER:

str1 = "The quick brown fox jumps over the lazy dog."
print(str1.split(' '))
str2 = "My name is Malik Shehryar and I am 18 years old"
print(str2.split(' '))


